#!/usr/bin/env node
/**
 * wrap-md-to-html.js — regenerate standalone .html wrappers for every .md in results/
 *
 * - Self-contained output: opens with double-click, no web server needed
 * - Markdown + Mermaid diagrams render client-side via CDN
 * - Idempotent: re-run anytime after a Document Mode write
 *
 * Usage:  node .claude/wrap-md-to-html.js
 */
const fs = require('fs');
const path = require('path');

const RESULTS_DIR = path.join(process.cwd(), 'results');

if (!fs.existsSync(RESULTS_DIR)) {
  process.stderr.write(`No results/ directory at ${RESULTS_DIR}. Nothing to wrap.\n`);
  process.exit(0);
}

const mdFiles = fs.readdirSync(RESULTS_DIR).filter(f => f.endsWith('.md'));
if (mdFiles.length === 0) {
  process.stderr.write(`No .md files in ${RESULTS_DIR}.\n`);
  process.exit(0);
}

function escapeHtml(s) {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

function htmlFor(title, rawMarkdown) {
  const safeTitle = escapeHtml(title);
  // Embed markdown as a script payload — preserves UTF-8 (em-dash, arrows, smart
  // quotes) which atob/btoa silently mangles. The </script> closer inside the
  // markdown is the only thing we need to escape.
  const safePayload = rawMarkdown.replace(/<\/script>/gi, '<\\/script>');

  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>${safeTitle}</title>
<script src="https://cdn.jsdelivr.net/npm/marked/marked.min.js"></script>
<script src="https://cdn.jsdelivr.net/npm/mermaid@10/dist/mermaid.min.js"></script>
<style>
  :root { color-scheme: light dark; }
  body {
    font-family: -apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, Helvetica, Arial, sans-serif;
    max-width: 960px; margin: 2rem auto; padding: 0 1.25rem; line-height: 1.55;
  }
  h1, h2, h3, h4 { line-height: 1.25; }
  h1 { border-bottom: 1px solid #ccc; padding-bottom: .25rem; }
  h2 { border-bottom: 1px solid #eee; padding-bottom: .2rem; margin-top: 2rem; }
  code { font-family: ui-monospace, SFMono-Regular, Menlo, Consolas, monospace; font-size: .92em; }
  pre { background: #f6f8fa; padding: 1rem; overflow: auto; border-radius: 6px; }
  pre code { background: transparent; padding: 0; }
  table { border-collapse: collapse; margin: 1rem 0; width: 100%; }
  th, td { border: 1px solid #ddd; padding: .45rem .6rem; text-align: left; vertical-align: top; }
  th { background: #f0f4f8; }
  blockquote { border-left: 4px solid #dbe2ea; color: #555; margin: 1rem 0; padding: .25rem 1rem; }
  .mermaid { text-align: center; margin: 1rem 0; }
  .mermaid-error {
    border: 1px solid #d73a49;
    background: #ffeef0;
    color: #86181d;
    padding: .75rem 1rem;
    border-radius: 6px;
    margin: 1rem 0;
    font-family: ui-monospace, SFMono-Regular, Menlo, Consolas, monospace;
    font-size: .85em;
    white-space: pre-wrap;
  }
  .mermaid-error strong { display: block; margin-bottom: .5rem; }
  @media (prefers-color-scheme: dark) {
    body { background: #0d1117; color: #e6edf3; }
    h1 { border-color: #30363d; }
    h2 { border-color: #21262d; }
    pre { background: #161b22; }
    th { background: #161b22; }
    th, td { border-color: #30363d; }
    blockquote { border-color: #30363d; color: #8b949e; }
    .mermaid-error { background: #2d0b0e; color: #ff8a93; border-color: #5a1d22; }
  }
  .footer { color: #888; font-size: .85em; margin-top: 3rem; border-top: 1px solid #eee; padding-top: 1rem; }
</style>
</head>
<body>
<article id="content"></article>
<div class="footer">Generated by Salesforce RCA AI Suite (Claude Code).</div>
<script id="md-source" type="text/markdown">${safePayload}</script>
<script>
  (async () => {
    const md = document.getElementById('md-source').textContent;

    // Pull mermaid blocks out before marked runs, so it doesn't escape them as code
    const mermaidBlocks = [];
    const pre = md.replace(/\`\`\`mermaid\\n([\\s\\S]*?)\`\`\`/g, (_, code) => {
      const i = mermaidBlocks.push(code.trim()) - 1;
      return \`<div class="mermaid-placeholder" data-index="\${i}"></div>\`;
    });

    document.getElementById('content').innerHTML = marked.parse(pre);

    // Wait for Mermaid UMD to load (script tag is non-async by default, but be defensive)
    const mermaidReady = () => typeof window.mermaid !== 'undefined';
    if (!mermaidReady()) {
      await new Promise(r => {
        const t = setInterval(() => { if (mermaidReady()) { clearInterval(t); r(); } }, 50);
        setTimeout(() => { clearInterval(t); r(); }, 5000);
      });
    }
    if (window.mermaid && typeof window.mermaid.initialize === 'function') {
      window.mermaid.initialize({ startOnLoad: false, theme: 'default', securityLevel: 'loose' });
    }

    // Render each diagram, replacing the placeholder with the rendered SVG.
    // On parse error, show the source and the error so the diagram is debuggable.
    const placeholders = document.querySelectorAll('.mermaid-placeholder');
    for (let i = 0; i < placeholders.length; i++) {
      const ph = placeholders[i];
      const code = mermaidBlocks[Number(ph.dataset.index)];
      const id = 'mermaid-' + i;
      try {
        if (!window.mermaid) throw new Error('Mermaid library failed to load from CDN — check network.');
        const { svg } = await window.mermaid.render(id, code);
        const wrap = document.createElement('div');
        wrap.className = 'mermaid';
        wrap.innerHTML = svg;
        ph.replaceWith(wrap);
      } catch (err) {
        const errEl = document.createElement('div');
        errEl.className = 'mermaid-error';
        const msg = (err && err.message) ? err.message : String(err);
        errEl.innerHTML = '<strong>Mermaid render failed</strong>' +
          escapeHtml(msg) + '\\n\\n--- diagram source ---\\n' + escapeHtml(code);
        ph.replaceWith(errEl);
      }
    }

    function escapeHtml(s) {
      return String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
    }
  })();
</script>
</body>
</html>
`;
}

let count = 0;
for (const md of mdFiles) {
  const mdPath = path.join(RESULTS_DIR, md);
  const htmlPath = mdPath.replace(/\.md$/, '.html');
  const raw = fs.readFileSync(mdPath, 'utf8');
  const title = (raw.match(/^#\s+(.+)$/m) || [, md.replace(/\.md$/, '')])[1].trim();
  fs.writeFileSync(htmlPath, htmlFor(title, raw));
  process.stdout.write(`✓ ${path.relative(process.cwd(), htmlPath)}\n`);
  count++;
}
process.stdout.write(`\nWrote ${count} HTML file(s).\n`);
